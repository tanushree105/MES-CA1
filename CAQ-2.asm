MOV 50H, #06H
MOV 51H, #04H
MOV R0, 50H
MOV R1, 51H

COMPARE:
MOV A, R0
JZ CHECK_R1

MOV A, R1
JZ A_GREATER

DEC R0
DEC R1
SJMP COMPARE

CHECK_R1:
MOV A, R1
JZ EQUAL
SJMP B_GREATER

A_GREATER:
MOV A, #01H
SJMP STOP

B_GREATER:
MOV A, #0FFH
SJMP STOP

EQUAL:
CLR A

STOP:
SJMP STOP
END